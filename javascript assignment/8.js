
// 08. Demonstrate the use of trim() to remove any trailing whitespace at the beginning and the end of a string.

let string = "     dileepa        ";

let fullTrim = string.trim();

let startTrim = string.trimStart();

let endTrim = string.trimEnd();

console.log(string);
console.log(string.length);



console.log(fullTrim);
console.log(fullTrim.length);

console.log(startTrim);
console.log(startTrim.length);

console.log(endTrim);
console.log(endTrim.length);




